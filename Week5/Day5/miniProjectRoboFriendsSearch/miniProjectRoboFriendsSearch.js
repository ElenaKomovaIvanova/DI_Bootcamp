let robotsHTML = document.getElementById("mainWindow");

function displayRodots (robots) {
    let html = robots.map((item) => {
        return `<div class = robot><img src=${item.image} alt=${item.username}><h2>${item.name}</h2><p>${item.email}</p></div>`;
    });
    console.log(html.join(""));
    robotsHTML.innerHTML = html.join("");
}

displayRodots(robots);

let searchHTML = document.getElementById("search");

searchHTML.addEventListener('keypress', function(event) {
    if (event.key === 'Enter') {
        event.preventDefault(); 
        let searchValue = searchHTML.value.toLowerCase();
        const robotSearch = robots.filter(item => item.name.toLowerCase().includes(searchValue));
        displayRodots(robotSearch); 
    }
});
